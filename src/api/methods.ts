import { CreateFormI } from 'components/frontend/form/dashboard/create-form/types'
import { OrdersI } from 'queries/orders/types'
import { FetchWrapper } from './axios'
import { API_ROUTES } from './routes'

///////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////// CAMPAIGNS /////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////// GET METHODS ///////////////////////////////////////////

// get campaigns
export const getCampaigns = () =>
	FetchWrapper.get({
		route: API_ROUTES.getCampaigns()
	})

// get campaign by slug
export const getCampaignsBySlug = (slug: string) =>
	FetchWrapper.get({
		route: API_ROUTES.getCampaignsBySlug(slug)
	})

// get campaign by id

export const getCampaignsById = (id: string) =>
	FetchWrapper.get({
		route: API_ROUTES.getCampaignsById(id)
	})

///////////////////////////////////// POST METHODS ///////////////////////////////////////////

// create campaign
export const createCampaign = (data: CreateFormI) =>
	FetchWrapper.post({
		route: API_ROUTES.createCampaign(),
		data
	})

///////////////////////////////////// PUT/PATCH METHODS ///////////////////////////////////////////

// update campaign
export const updateCampaign = (id: string, data: CreateFormI) =>
	FetchWrapper.patch({
		route: API_ROUTES.updateCampaign(id),
		data
	})

///////////////////////////////////// DELETE METHODS ///////////////////////////////////////////

// DELETE campaign
export const deleteCampaign = (id: string) =>
	FetchWrapper.delete({
		route: API_ROUTES.updateCampaign(id)
	})

///////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////// ORDERS ///////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////// GET METHODS ///////////////////////////////////////////

// get all orders
export const getOrders = () =>
	FetchWrapper.get({
		route: API_ROUTES.getOrders()
	})

///////////////////////////////////// POST METHODS ///////////////////////////////////////////

// create orders
export const createOrder = (data: OrdersI) =>
	FetchWrapper.post({
		route: API_ROUTES.createOrder(),
		data
	})

